// ===============================================
// 課題04: 配列問題（合計と最大値）
// ===============================================
// テストの点数が入った配列から、
//   合計
//   最大値
// を求めます。
//
// 実行方法:
//   npx tsx src/04-array.ts
//
// 学ぶこと:
//   - 配列 … たくさんの値を1つの箱にまとめて持つ
//   - for  … 配列を「先頭から1つずつ」見ていく
//   - if   … 今見ている値が今までの最大より大きいか比べる
// ===============================================

const scores = [80, 90, 70];

// --- 合計を求める ---
// 「足し算の途中結果」をためる変数。最初は 0。
let total = 0;
for (let i = 0; i < scores.length; i++) {
  // scores[i] は「配列の i 番目の値」。番号は 0 から始まる。
  total = total + scores[i];
}
console.log(`合計: ${total}`);

// --- 最大値を求める ---
// 「今のところ一番大きい値」をためる変数。
// とりあえず先頭の値を入れておくのがコツ。
let max = scores[0];
for (let i = 0; i < scores.length; i++) {
  if (scores[i] > max) {
    // 今見ている値の方が大きければ、max を更新する
    max = scores[i];
  }
}
console.log(`最大値: ${max}`);

