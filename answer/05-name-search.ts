/*
  課題05: 名前検索

  名前の配列の中に、探したい名前があるかどうかを調べます。

  実行方法:
    npx tsx src/05-name-search.ts

  学ぶこと:
    - 配列探索 … 配列を先頭から見て、目的のものを探す
    - 「見つけた」という状態を変数で覚えておく
*/

const names = ["田中", "佐藤", "鈴木"];
const target = "佐藤"; // 探したい名前

// 「見つかったかどうか」を覚えておく変数。最初は false（まだ見つかってない）。
let found = false;

for (let i = 0; i < names.length; i++) {
  if (names[i] === target) {
    // 見つかったら、しるしを true にする
    found = true;
  }
}

// ループが終わったあとで、結果を表示する
if (found) {
  console.log(`${target}を見つけました`);
} else {
  console.log(`${target}は見つかりませんでした`);
}

