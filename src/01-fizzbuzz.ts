// ===============================================
// 課題01: FizzBuzz
// ===============================================
// 1 から 100 までの数を、順番に表示します。
// ただし……
//   3 で割り切れる数        → "Fizz"
//   5 で割り切れる数        → "Buzz"
//   3 でも 5 でも割り切れる → "FizzBuzz"
//
// 実行方法:
//   npx tsx src/01-fizzbuzz.ts
//
// 学ぶこと:
//   - for … 繰り返し（同じ処理を何回も行う）
//   - if  … 条件分岐（〜だったら、こうする）
//   - %   … 割ったときの「あまり」を求める記号
// ===============================================

// for は「i を 1 から始めて、100 になるまで 1 ずつ増やしながら繰り返す」
for (let i = 1; i <= 100; i++) {
  // i % 3 は「i を 3 で割ったあまり」。
  // あまりが 0 なら、ぴったり割り切れたということ。
  if (i % 3 === 0 && i % 5 === 0) {
    // && は「かつ」。3でも5でも割り切れるとき
    console.log("FizzBuzz");
  } else if (i % 3 === 0) {
    console.log("Fizz");
  } else if (i % 5 === 0) {
    console.log("Buzz");
  } else {
    // どの条件にも当てはまらなければ、数字をそのまま表示
    console.log(i);
  }
}

